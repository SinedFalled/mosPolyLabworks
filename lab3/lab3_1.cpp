#include <iostream>
using namespace std;


int main() {
// 1. Создаём переменную
int number = 42;


// 2. Объявляем указатель на переменную
int* ptr = &number;


// 3. Выводим адрес указателя и значение по этому адресу
cout << "Адрес указателя: " << ptr << endl;
cout << "Значение по адресу: " << *ptr << endl;


// 4. Изменяем значение через указатель
*ptr = 99;


// 5. Проверяем, что переменная изменила значение
cout << "Новое значение переменной number: " << number << endl;


// 6. Создаём массив
int arr[5] = {10, 20, 30, 40, 50};


// 7. Выводим элементы массива через указатель
int* pArr = arr; // имя массива — это указатель на его первый элемент
cout << "Элементы массива: ";
for (int i = 0; i < 5; i++) {
cout << *(pArr + i) << " "; // получаем элементы с помощью арифметики указателей
}
cout << endl;


// 8. Объявляем переменную
int a = 5;


// 9. Создаём указатель, который нельзя переназначить (константный адрес), но можно менять значение
int* const constPtr = &a;


// Меняем значение через указатель
*constPtr = 15;


// 10. Проверяем результат
cout << "Новое значение переменной a: " << a << endl;


// Попытка изменить адрес вызовет ошибку компиляции (строка закомментирована):
// constPtr = &number; // Ошибка: нельзя изменить адрес, на который ссылается constPtr


return 0;
}