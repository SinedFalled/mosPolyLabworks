#include <iostream> // Подключаем библиотеку для ввода вывода данных
using namespace std; // Используем пространство имён std, чтобы не писать std:: перед каждым элементом

int main() { // Точка входа в программу
    // --- Задание 1 ---
    int number; // Объявляем переменную для хранения числа, введённого пользователем
    while (true) { // Запускаем бесконечный цикл
        cout << "Введите целое положительное число: "; // Просим пользователя ввести число
        cin >> number; // Считываем введённое значение
        if (number > 0) // Проверяем, положительное ли число
            break; // ыходим из цикла
        cout << "Число должно быть положительным! Попробуйте снова.\n"; // Сообщаем об ошибке и повторяем ввод
    }

    // --- Задание 2 ---
    int sum = 0; // Переменная для накопления суммы
    cout << "Сумма чисел от 1 до " << number << ": "; // Выводим заголовок
    for (int i = 1; i <= number; i++) { // Цикл от 1 до введённого числа
        cout << i; // Выводим текущее число
        if (i < number) cout << " + "; // Добавляем знак + между числами
        sum += i; // Прибавляем текущее число к сумме
    }
    cout << " = " << sum << endl; // Выводим итоговую сумму

    // --- Задание 3 ---
    int arr[10] = {5, 12, 7, 3, 9, 14, 6, 2, 8, 10}; // Создаём массив из 10 чисел

    // --- Задание 4 ---
    cout << "Все элементы массива: "; // Заголовок
    for (int i = 0; i < 10; i++) { // Проходим по всем элементам массива
        cout << arr[i] << " "; // Выводим значение элемента с пробелом
    }
    cout << endl; // Переход на новую строку

    // --- Задание 5 ---
    cout << "Числа на чётных позициях: "; // Заголовок
    for (int i = 0; i < 10; i++) { // Цикл по всем индексам массива
        if (i % 2 == 0) // Проверяем, чётный ли индекс
            cout << arr[i] << " "; // Выводим элемент на чётной позиции
    }
    cout << endl; // Переход на новую строку

    // --- Задание 6 ---
    int oddSum = 0; // Переменная для суммы нечётных позиций
    for (int i = 0; i < 10; i++) { // Цикл по всем элементам массива
        if (i % 2 != 0) // Проверяей нечётный ли индекс
            oddSum += arr[i]; // Добавляем элемент к сумме
    }
    cout << "Сумма элементов на нечётных позициях: " << oddSum << endl; // Выводим результат

    return 0; // Возвращаем 0 сигнализируя об успешном завершении программы
} // Конец функции main
