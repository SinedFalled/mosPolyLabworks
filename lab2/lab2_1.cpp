#include <iostream> // подключаем библиотеку для ввода/вывода

int main() // точка входа в программу
{ // начало функции main
    std::setlocale(LC_ALL, ""); // устанавливаем локаль системы

    // Задание 1. Получение двух чисел от пользователя
    double num1, num2; // создаём две переменные для чисел
    std::cout << "Введите первое число: "; // просим пользователя ввести первое число
    std::cin >> num1; // сохраняем введённое значение в num1
    std::cout << "Введите второе число: "; // просим пользователя ввести второе число
    std::cin >> num2; // сохраняем введённое значение в num2

    // Задание 2. Вычисление и вывод среднего арифметического
    double avg = (num1 + num2) / 2.0; // вычисляем среднее арифметическое
    std::cout << "Среднее арифметическое: " << avg << std::endl; // выводим результат

    // Задание 3. Ввод знака операции
    char op; // переменная для хранения знака операции
    std::cout << "Введите знак операции (+, -, *, /): "; // запрос операции
    std::cin >> op; // сохраняем введённый символ

    // Задание 4. Выполнение операции через if-else (закомментировано)
    /*
    if (op == '+') std::cout << "Результат: " << num1 + num2 << std::endl;
    else if (op == '-') std::cout << "Результат: " << num1 - num2 << std::endl;
    else if (op == '*') std::cout << "Результат: " << num1 * num2 << std::endl;
    else if (op == '/') {
        if (num2 != 0) std::cout << "Результат: " << num1 / num2 << std::endl;
        else std::cout << "Ошибка: деление на ноль" << std::endl;
    }
    else std::cout << "Неизвестная операция" << std::endl;
    */

    // Задание 5. Выполнение операции через switch-case
    switch (op) {
        case '+':
            std::cout << "Результат: " << num1 + num2 << std::endl;
            break;
        case '-':
            std::cout << "Результат: " << num1 - num2 << std::endl;
            break;
        case '*':
            std::cout << "Результат: " << num1 * num2 << std::endl;
            break;
        case '/':
            if (num2 != 0)
                std::cout << "Результат: " << num1 / num2 << std::endl;
            else
                std::cout << "Ошибка: деление на ноль" << std::endl;
            break;
        default:
            std::cout << "Неизвестная операция" << std::endl;
    }

    return 0; // завершение программы
} // конец функции main
